    @SuppressWarnings("unchecked")
    public void carregaTabela() {

        DefaultTableModel modelo = (DefaultTableModel) this.tabela_aluno.getModel();
        modelo.setNumRows(0);

        List<Aluno> minhalista = new ArrayList<>();
        minhalista = objaluno.getAlunos();

        for (Aluno a : minhalista) {
            modelo.addRow(new Object[]{
                a.getMatricula(),
                a.getNome(),
                a.getDataNascimento(),
                a.getRg(),
                a.getCpf(),
                a.getCurso()});
        }
    }

