    private void botao_alterarAlunoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botao_alterarAlunoActionPerformed
        try {
            int matricula = 0;
            String nome = "";
            LocalDate nascimento = null;
            String rg = "";
            String cpf = "";
            int fase = 0;
            String curso = "";

            if (this.c_nomeAluno1.getText().length() < 2) {
                throw new Mensagens("Nome deve conter ao menos 2 caracteres");
            } else {
                nome = this.c_nomeAluno1.getText();
            }

            if (this.c_dataNascAluno1.getText().length() != 10) {
                throw new Mensagens("Data deve conter 10 caracteres");
            } else {
                nascimento = DateFormat.converterDataSQL(this.c_dataNascAluno1.getText());
            }

            rg = this.c_rgAluno1.getText();
            cpf = this.c_cpfAluno2.getText();
            

            if (Integer.parseInt(this.c_faseAluno2.getText()) <= 0) {
                throw new Mensagens("Fase deve ser número e maior que zero");
            } else {
                fase = Integer.parseInt(this.c_faseAluno2.getText());
            }

            if (this.c_curso.getText().length() < 2) {
                throw new Mensagens("Curso deve conter mais de 2 caracteres");
            } else {
                curso = this.c_curso.getText();
            }

            if (this.tabela_aluno.getSelectedRow() == -1) {
                throw new Mensagens("Primeiro selecione um aluno para alterar");
            } else {
                matricula = Integer.parseInt(this.tabela_aluno.getValueAt(this.tabela_aluno.getSelectedRow(), 0).toString());
            }

            //envia os dados para o controlador processar
            if (this.objaluno.UpdateAlunoBD(fase, curso, matricula, nome, nascimento, rg, cpf)) {
                //limpa os campos
                this.c_nomeAluno1.setText("");
                this.c_dataNascAluno1.setText("");
                this.c_rgAluno1.setText("");
                this.c_cpfAluno2.setText("");
                this.c_faseAluno2.setText("");
                this.c_curso.setText("");
                JOptionPane.showMessageDialog(rootPane, "Aluno alterado com Sucesso");
            }
            System.out.println(this.objaluno.getAlunos().toString());
        } catch (Mensagens erro) {
            JOptionPane.showMessageDialog(null, erro.getMessage());
        } catch (NumberFormatException erro2) {
            JOptionPane.showMessageDialog(null, "Informe um número");
        } finally {
            carregaTabela();
        }


    }//GEN-LAST:event_botao_alterarAlunoActionPerformed

    private void botao_apagarAlunoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botao_apagarAlunoActionPerformed
        try {
            //validando dados da interface gráfica
            int matricula = 0;
            if (this.tabela_aluno.getSelectedRow() == -1) {
                throw new Mensagens("Primeiro Selecione um Aluno para APAGAR");
            } else {
                matricula = Integer.parseInt(this.tabela_aluno.getValueAt(this.tabela_aluno.getSelectedRow(), 0).toString());
            }

            int resposta_usuario = JOptionPane.showConfirmDialog(null, "Tem certeza que deseja APAGAR este aluno?");

            if (resposta_usuario == 0) {
                if (this.objaluno.DeletetAlunoBD(matricula)) {
                    this.c_nomeAluno1.setText("");
                    this.c_dataNascAluno1.setText("");
                    this.c_rgAluno1.setText("");
                    this.c_cpfAluno2.setText("");
                    this.c_faseAluno2.setText("");
                    this.c_curso.setText("");
                    JOptionPane.showMessageDialog(rootPane, "Aluno apagado com sucesso");
                }
            }
            System.out.println(this.objaluno.getAlunos().toString());
        } catch (Mensagens erro) {
            JOptionPane.showMessageDialog(null, erro.getMessage());
        } finally {
            this.carregaTabela();
        }

    }//GEN-LAST:event_botao_apagarAlunoActionPerformed

